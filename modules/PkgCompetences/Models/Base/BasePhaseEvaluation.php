namespace Modules\PkgCompetences\Models\Base;

use Illuminate\Database\Eloquent\Factories\HasFactory;
use Illuminate\Database\Eloquent\Model;
use Illuminate\Database\Eloquent\Relations\BelongsTo;
use Illuminate\Database\Eloquent\Relations\HasMany;
use App\Traits\OwnedByUser;
use App\Traits\HasDynamicContext;
use Modules\Core\Models\BaseModel;
use Modules\PkgCompetences\Models\CritereEvaluation;

/**
 * Classe BasePhaseEvaluation
 * Cette classe sert de base pour le modèle PhaseEvaluation.
 */
class BasePhaseEvaluation extends BaseModel
{
    use HasFactory, HasDynamicContext;


    public function __construct(array $attributes = []) {
        parent::__construct($attributes); 
        $this->isOwnedByUser =  false;
    }

    
    /**
     * Les attributs remplissables pour le modèle.
     *
     * @var array
     */
    protected $fillable = [
        'ordre', 'code', 'libelle', 'coefficient', 'description'
    ];




    /**
     * Relation HasMany pour PhaseEvaluations.
     *
     * @return HasMany
     */
    public function critereEvaluations(): HasMany
    {
        return $this->hasMany(CritereEvaluation::class, 'phase_evaluation_id', 'id');
    }



    /**
     * Méthode __toString pour représenter le modèle sous forme de chaîne.
     *
     * @return string
     */
    public function __toString()
    {
        return $this->code ?? "";
    }
}
